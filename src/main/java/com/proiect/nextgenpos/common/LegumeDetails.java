package com.proiect.nextgenpos.common;

/**
 *
 * @author adrian
 */
public class LegumeDetails implements java.io.Serializable {
    private Integer id;
    private String name;
    private Integer price;

    public LegumeDetails(Integer id, String name, Integer price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getPrice() {
        return price;
    }

    public void setPrice(Integer price) {
        this.price = price;
    }
    
    
    
}
